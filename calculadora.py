def sumar(parametro_1,parametro_2):
    """ 
    Método encargado de sumar dos numeros
    Args:
      parametro_1 (numeric): primer valor de la suma
      parametro_2 (numeric): segundo valor de la suma
    """
    return parametro_1 + parametro_2

def restar(parametro_1,parametro_2):
    """ 
    Método encargado de restar dos numeros
    Args:
      parametro_1 (numeric): primer valor de la resta
      parametro_2 (numeric): segundo valor de la resta
    """
    return parametro_1 - parametro_2

def multiplicar(parametro_1,parametro_2):
    """ 
    Método encargado de multiplicar dos numeros
    Args:
      parametro_1 (numeric): primer valor de la multiplicación
      parametro_2 (numeric): segundo valor de la multiplicación
    """
    return parametro_1 * parametro_2
